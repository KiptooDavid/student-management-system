    private void btnSaveRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveRecordActionPerformed
        try {
            String id = textID.getText();
            int newID = Integer.parseInt(id);
            String full = textFullName.getText();
            String reg = textRegNo.getText();
            int newReg = Integer.parseInt(reg);
            String gender = textGender.getText();
            String email = textEmail.getText();
            
            rs.moveToInsertRow();
            
            rs.updateInt("ID_NO", newID);
            rs.updateString("Full_Name", full);
            rs.updateInt("REG_NO", newReg);
            rs.updateString("Gender", gender);
            rs.updateString("Email", email);
            
            rs.insertRow();
            
            stmt.close();
            rs.close();
            
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            
            String sql = "SELECT * FROM student";
            rs = stmt.executeQuery(sql);
            
            rs.next();
            int id_no = rs.getInt("ID_NO");
            String id2 = Integer.toString(id_no);
            String full2 = rs.getString("Full_Name");
            int reg_no = rs.getInt("REG_NO");
            String reg2 = Integer.toString(reg_no);
            String gender2 = rs.getString("Gender");
            String email2 = rs.getString("Email");
            
            textID.setText(id2);
            textFullName.setText(full2);
            textRegNo.setText(reg2);
            textGender.setText(gender2);
            textEmail.setText(email2);
            
        } catch (SQLException err) {
            System.out.println(err.getMessage());
        }
        
    }//GEN-LAST:event_btnSaveRecordActionPerformed

