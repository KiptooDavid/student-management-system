    public void doConnect(){
        try {
            //connect to the database..
            String host = "jdbc:derby://localhost:1527/Student";
            String uName = "student";
            String uPass = "student";
            
            con = DriverManager.getConnection(host, uName, uPass);
            
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            String sql = "SELECT * FROM student";
            rs = stmt.executeQuery(sql);
            
            rs.next();
            int id_no = rs.getInt("ID_NO");
            String id = Integer.toString(id_no);
            String full = rs.getString("Full_Name");
            int reg_no = rs.getInt("REG_NO");
            String reg = Integer.toString(reg_no);
            String gender = rs.getString("Gender");
            String email = rs.getString("Email");
            
            textID.setText(id);
            textFullName.setText(full);
            textRegNo.setText(reg);
            textGender.setText(gender);
            textEmail.setText(email);
            
        } catch (SQLException err) {
            JOptionPane.showConfirmDialog(student.this, err.getMessage());
        }
        
    }

